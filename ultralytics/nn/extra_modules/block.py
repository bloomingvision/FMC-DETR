class FFT_AmplitudeModulator(nn.Module):
    def __init__(self, dim) -> None:
        super().__init__()
        self.pwconv1 = nn.Conv2d(dim, dim, 1, 1, groups=1)
        # 2. 振幅调制器 (Amplitude Modulator)
        self.amp_modulator = nn.Sequential(
            nn.Conv2d(dim, dim, 1, 1, groups=1),
            nn.LeakyReLU(0.1, inplace=True),
            nn.Conv2d(dim, dim, 1, 1, groups=1)
        )
        # 3. 可学习的残差加权参数
        self.alpha = nn.Parameter(torch.zeros(dim, 1, 1))
        self.beta = nn.Parameter(torch.ones(dim, 1, 1))

    def forward(self, x):
        B, C, H, W = x.shape
        x_in = x 
        x = self.pwconv1(x)
        # --- A. 转频域 (rfft2) ---
        x_fft = torch.fft.rfft2(x, norm='backward') 
        # --- B. 振幅与相位解耦 (核心优化) ---
        # 替代之前的 x_fft.real 和 x_fft.imag
        amp = torch.abs(x_fft)          # 振幅：包含纹理、亮度特征
        phase = torch.angle(x_fft)      # 相位：包含边缘、结构特征
        # --- C. 频域处理 (仅调制振幅) ---
        # 保护相位不受非线性激活函数的扭曲，维持结构信息
        amp_out = self.amp_modulator(amp)
        # --- D. 利用欧拉公式重新组合为复数 ---
        out_complex = amp_out * torch.exp(1j * phase)
        # --- E. 逆变换 (irfft2) ---
        out = torch.fft.irfft2(out_complex, s=(H, W), norm='backward')
        # --- F. 残差融合 ---
        return out * self.alpha + x_in * self.beta

