class AGDD(nn.Module):  
    def __init__(self, c1, c2):  
        super().__init__()
        self.c = c2                 
        self.cv1 = Conv(c1 // 2, c1 // 2, 3, 2, 1)  
        self.cv2 = Conv(c1 // 2, self.c, 3, 1, 1)    
        self.adjust = Conv(c1 // 2, self.c, 1)       
        self.ffm = FFT_AmplitudeModulator(self.c)  
        self.cv3 = Conv(self.c, self.c, 1, 1)
        
    def forward(self, x):
        x1, x2 = x.chunk(2, 1)  
        x1_s1 = self.cv1(x1)         # [B, c, H//2, W//2]
        x1_spatial = self.cv2(x1_s1) # [B, c, H//2, W//2] 64
        x2_pool = F.avg_pool2d(x2, 2, 2)         # [B, c1//2, H//2, W//2]
        x2_adjusted = self.adjust(x2_pool)        # [B, c, H//2, W//2] 通道对齐
        fgm_out = self.ffm(x2_adjusted)           # [B, c, H//2, W//2] 64
        out_all = x1_spatial * fgm_out
        y = self.cv3(out_all)  # [B, c, H//2, W//2] 64
        return y

class SobelConv(nn.Module):
    def __init__(self, channel) -> None:
        super().__init__()
        
        sobel = np.array([[1, 2, 1], [0, 0, 0], [-1, -2, -1]])
        sobel_kernel_y = torch.tensor(sobel, dtype=torch.float32).unsqueeze(0).expand(channel, 1, 1, 3, 3)
        sobel_kernel_x = torch.tensor(sobel.T, dtype=torch.float32).unsqueeze(0).expand(channel, 1, 1, 3, 3)
        
        self.sobel_kernel_x_conv3d = nn.Conv3d(channel, channel, kernel_size=3, padding=1, groups=channel, bias=False)
        self.sobel_kernel_y_conv3d = nn.Conv3d(channel, channel, kernel_size=3, padding=1, groups=channel, bias=False)
        
        self.sobel_kernel_x_conv3d.weight.data = sobel_kernel_x.clone()
        self.sobel_kernel_y_conv3d.weight.data = sobel_kernel_y.clone()
        
        self.sobel_kernel_x_conv3d.requires_grad = False
        self.sobel_kernel_y_conv3d.requires_grad = False

    def forward(self, x):
        return (self.sobel_kernel_x_conv3d(x[:, :, None, :, :]) + self.sobel_kernel_y_conv3d(x[:, :, None, :, :]))[:, :, 0]

class Multi_Domain_Refinement(nn.Module):
    def __init__(self, dim) -> None:
        super().__init__()
        self.freq_conv = nn.Conv2d(dim * 2, dim * 2, kernel_size=1, stride=1, padding=0, groups=1)
        self.freq_act = nn.LeakyReLU(0.1, inplace=True)

        self.pool = nn.AdaptiveAvgPool2d((1, 1))
        self.conv = nn.Conv2d(dim, dim, kernel_size=1, padding=0, stride=1, bias=True)
        self.sober = SobelConv(dim) 

        self.spa = DWConv(dim, dim, 3, 1)

        self.out_conv = Conv(dim, dim, 1)

    def forward(self, x):
        x_spa = self.spa(x)
        B, C, H, W = x.shape
        # ==========================================
        # FCA 分支：基于实虚部拼接的频域卷积
        # ==========================================
        # 1. 转频域 (rfft2)
        x_fft = torch.fft.rfft2(x, norm='backward')
        
        # 2. 拼接实部和虚部: [B, C, H, W_half] -> [B, 2C, H, W_half]
        x_cat = torch.cat([x_fft.real, x_fft.imag], dim=1)
        
        # 3. 频域 1x1 卷积处理 (混合通道与实虚部)
        freq_out = self.freq_conv(x_cat)
        freq_out = self.freq_act(freq_out)
        
        # 4. 拆分回实部和虚部
        out_real, out_imag = torch.chunk(freq_out, 2, dim=1)
        
        # 5. 组合回复数
        out_complex = torch.complex(out_real, out_imag)
        
        # 6. 逆变换 (irfft2) 并取绝对值稳定输出
        x_fca = torch.fft.irfft2(out_complex, s=(H, W), dim=(-2, -1), norm='backward')
        x_fca = torch.abs(x_fca) 
        # ==========================================
        # SCA 分支：空间边缘调制 (串行依赖 x_fca)
        # ==========================================
        att_sca = torch.sigmoid(self.conv(self.pool(x_fca)))
        out_sober = self.sober(x)
        x_sca = att_sca * out_sober
        # ==========================================
        # 最终特征融合 (Residual Fusion)
        # ==========================================
        out_fused = x_spa + x_fca + x_sca 
        out_fused = self.out_conv(out_fused)
        return x + out_fused

class MDFC(nn.Module):
    def __init__(self, dim, e=0.25):
        super().__init__()
        self.e = e
        self.cv1 = Conv(dim, dim, 1)
        self.cv2 = Conv(dim, dim, 1)
        self.m = Multi_Domain_Refinement(int(dim * self.e))

    def forward(self, x):
        ok_branch, identity = torch.split(self.cv1(x), [int(x.size(1) * self.e), int(x.size(1) * (1 - self.e))], dim=1)
        return self.cv2(torch.cat((self.m(ok_branch), identity), 1))